import turtle
import math

t = turtle.pen()
t = turtle

t.up()
t.goto(0, 150)
t.down()
t.color('red')
t.begin_fill()
t.fillcolor('red')
t.speed(1)
t.left(45)
t.forward(150)
t.right(45)
t.forward(100)
t.right(45)
t.forward(100)
t.right(45)
t.forward(100)
t.right(45)

t.forward(250 + math.sqrt(2) * 100)
t.right(90)
t.speed(2)
t.forward(250 + math.sqrt(2) * 100)
t.right(45)
t.forward(100)
t.right(45)
t.forward(100)
t.right(45)
t.forward(100)
t.right(45)
t.forward(150)
t.end_fill()

# F
t.goto(-10, 0)
t.pencolor('orange')
